function App() {
  //объявление констант
  const [activeIndex, setActiveIndex] = useState(null);
  const [yearRange, setYearRange] = useState([1987, 1992]);
  const [currentPage, setCurrentPage] = useState(1);
  const EVENTS_PER_PAGE = 3;
  //вычисления
  const CENTER_X = 452 + 530 / 2;
  const CENTER_Y = 215 + 530 / 2;
  const RADIUS = 530 / 2;
  //вычисления годов
  const rangeKey = `${yearRange[0]}-${yearRange[1]}`;
  //Категории для годов
  const availableCategories = eventsByYearRange[rangeKey] ? Object.keys(eventsByYearRange[rangeKey]) : [];
  //Порядок категорий
  const labels = availableCategories
    .filter(cat => cat !== "Наука")
    .concat(availableCategories.includes("Наука") ? ["Наука"] : []);
  //Координаты для каждой точки
  const points = labels.map((label, i) => {
    const angle = (i / labels.length) * 2 * Math.PI - Math.PI / 2;
    const x = CENTER_X + RADIUS * Math.cos(angle);
    const y = CENTER_Y + RADIUS * Math.sin(angle);
    return { x, y, label };
  });
  const selectedLabel = activeIndex !== null ? labels[activeIndex] : (labels.includes("Наука") ? "Наука" : labels[0]);
  const events = eventsByYearRange[rangeKey]?.[selectedLabel] || [];
  const totalPages = Math.ceil(events.length / EVENTS_PER_PAGE);
  const pagedEvents = events.slice((currentPage - 1) * EVENTS_PER_PAGE, currentPage * EVENTS_PER_PAGE);
  //Переход на год раньше
  const handleNextRange = () => {
    if (yearRange[0] === 1987) {
      setYearRange([1992, 1997]);
      setActiveIndex(null);
      setCurrentPage(1);
    } else if (yearRange[0] === 1992) {
      setYearRange([1997, 2005]);
      setActiveIndex(null);
      setCurrentPage(1);
    } 
  };
  //Переход на год позже
  const handlePreviousRange = () => {
    if (yearRange[0] === 1997) {
      setYearRange([1992, 1997]);
      setActiveIndex(null);
      setCurrentPage(1);
    } else if (yearRange[0] === 1992) {
      setYearRange([1987, 1992]);
      setActiveIndex(null);
      setCurrentPage(1);
    } 
  };
  //Переключение событий
  const handlePreviousPage = () => {
    if (currentPage > 1) {
      setCurrentPage(currentPage - 1);
    } else {
      setCurrentPage(totalPages);
    }
  };
  //Переключение на следующую страницу событий
  const handleNextPage = () => {
    if (currentPage < totalPages) {
      setCurrentPage(currentPage + 1);
    } else {
      setCurrentPage(1);
    }
  };
  //основной блок кода для страницы
  return (
    <div className="container">
      <div className="wrapper">
        <h1 className="history_name">
          <span className="history_span">
            <svg width="6" height="120" viewBox="0 0 6 120" fill="none" xmlns="http://www.w3.org/2000/svg">
              <path d="M3 0L2.99999 120" stroke="url(#paint0_linear_1_56)" strokeWidth="5" />
              <defs>
                <linearGradient id="paint0_linear_1_56" x1="3.5" y1="-6" x2="3.49999" y2="102" gradientUnits="userSpaceOnUse">
                  <stop stopColor="#3877EE" />
                  <stop offset="1" stopColor="#EF5DA8" />
                </linearGradient>
              </defs>
            </svg>
          </span>
          Исторические даты
        </h1>
      </div>
      <div className="circle_background">
        <div className="circle"></div>
        {points.map(({ x, y, label }, idx) => {
          if (activeIndex === idx) {
            return (
              <React.Fragment key={idx}>
                <div
                  className="since"
                  style={{
                    left: x - 56 / 2,
                    top: y - 56 / 2,
                    display: "flex",
                    alignItems: "center",
                    justifyContent: "center",
                    fontWeight: "bold",
                    fontSize: "16px",
                    color: "#42567A",
                    cursor: "pointer",
                  }}
                  onClick={() => setActiveIndex(null)}
                  title={label}
                >
                  {idx + 1}
                </div>
                <div
                  className="label-next-to-circle"
                  style={{ left: x + 56 / 2 + 8, top: y - 12 / 2 }}
                >
                  {label}
                </div>
              </React.Fragment>
            );
          }
          return (
            <div
              key={idx}
              className="circle_point"
              style={{ top: y, left: x, cursor: "pointer" }}
              onClick={() => {
                setActiveIndex(idx);
                setCurrentPage(1);
              }}
              title={label}
            ></div>
          );
        })}
        <div className="line_horizontal"></div>
        <div className="line_vertical"></div>
        <div className="year">
          <div className="blue">{yearRange[0]}</div>
          <div className="pink">{yearRange[1]}</div>
        </div>
      </div>
      <div className="since_wrapper">
        <div className="since">
          <span className="since_number">{activeIndex !== null ? activeIndex + 1 : labels.indexOf(selectedLabel) + 1}</span>
        </div>
        <h2 className="title_s">{selectedLabel}</h2>
      </div>
      <div className="data">
        <h3 className="title_data">
          {totalPages > 0 ? `${String(currentPage).padStart(2, '0')}/${String(totalPages).padStart(2, '0')}` : "—"}
        </h3>
        <button
          className="button_left"
          onClick={handlePreviousRange} 
          disabled={yearRange[0] === 1987}
          title="Предыдущий диапазон"
        >
          <svg width="10" height="14" viewBox="0 0 10 14" fill="none" xmlns="http://www.w3.org/2000/svg">
            <path d="M8.49988 0.750001L2.24988 7L8.49988 13.25" stroke="#42567A" strokeWidth="2" />
          </svg>
        </button>
        <button
          className="button_right"
          onClick={handleNextRange}
          disabled={yearRange[0] === 2005}
          title="Следующий диапазон"
        >
          <svg width="10" height="14" viewBox="0 0 10 14" fill="none" xmlns="http://www.w3.org/2000/svg">
            <path d="M1.50012 0.750001L7.75012 7L1.50012 13.25" stroke="#42567A" strokeWidth="2" />
          </svg>
        </button>
      </div>
      <div className="sentense">
        {pagedEvents.length > 0 ? (
          pagedEvents.map((item, idx) => (
            <div className="sentence" key={idx}>
              <h3 className="year_sentence">{item.year}</h3>
              <p className="description">{item.event}</p>
            </div>
          ))
        ) : (
          <p>События не найдены</p>
        )}
        <button className="button_sentense" onClick={handlePreviousPage} style={{ marginRight: 8 }} title="Предыдущая страница">
          <svg width="8" height="12" viewBox="0 0 8 12" fill="none" xmlns="http://www.w3.org/2000/svg">
            <path d="M7 1L2 6L7 11" stroke="#3877EE" strokeWidth="2" />
          </svg>
        </button>
        <button className="button_sentense" onClick={handleNextPage} title="Следующая страница">
          <svg width="8" height="12" viewBox="0 0 8 12" fill="none" xmlns="http://www.w3.org/2000/svg">
            <path d="M1 1L6 6L1 11" stroke="#3877EE" strokeWidth="2" />
          </svg>
        </button>
      </div>
      <div className="button_sentense_mobile">
        {Array(totalPages).fill(null).map((_, idx) => (
          <button
            key={idx}
            className="button_mobile"
            style={{ opacity: currentPage === idx + 1 ? 1 : 0.4 }}
            onClick={() => setCurrentPage(idx + 1)}
            title={`Страница ${idx + 1}`}
          >
            <svg width="6" height="6" viewBox="0 0 6 6" fill="none" xmlns="http://www.w3.org/2000/svg">
              <circle cx="3" cy="3" r="3" fill="#42567A"/>
            </svg>
          </button>
        ))}
      </div>
    </div>
  );
}
